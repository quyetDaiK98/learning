package org.vti.DataStructureAndAlgorithms;

public class MyTreeNode {
	public int data;
	public MyTreeNode leftChild;
	public MyTreeNode rightChild;
	
	
	public MyTreeNode(int data, MyTreeNode leftChild, MyTreeNode rightChild) {
		super();
		this.data = data;
		this.leftChild = leftChild;
		this.rightChild = rightChild;
	}
	
}
